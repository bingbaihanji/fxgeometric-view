package com.binbaihanji.constant;

/**
 * 绘制模式枚举
 *
 * @author bingbaihanji
 * @date 2025-12-21
 */
public enum DrawMode {
    /**
     * 无绘制模式
     */
    NONE,

    /**
     * 点
     */
    POINT,

    /**
     * 线段
     */
    LINE,

    /**
     * 圆
     */
    CIRCLE,

    /**
     * 三角形
     */
    TRIANGLE,

    /**
     * 矩形
     */
    RECTANGLE,

    /**
     * 多边形（边数可配置）
     */
    POLYGON
}
